# --- –ù–ê–°–¢–†–û–ô–ö–ê –°–¢–†–ê–ù–ò–¶–´ ---
st.set_page_config(page_title="–ú–æ–¥–µ–ª—é–≤–∞–Ω–Ω—è —Å—Ç—Ä—É–º—É", layout="wide")
st.title("üî¨ –ú–û–î–ï–õ–Æ–í–ê–ù–ù–Ø –î–ò–ù–ê–ú–Ü–ö–ò –ì–£–°–¢–ò–ù–ò –°–¢–†–£–ú–£")
